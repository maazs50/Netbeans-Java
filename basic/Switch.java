package basic;

public class Switch {
public static void main(String args[]){
    int choice =1;
    switch(choice){
        case 0:
            System.out.print("8 bits");
            break;
            
        case 01:
            System.out.print("16 bits");
            break;
            
        case 2:
            System.out.print("32 bits");
            break;
            
        case 3:
            System.out.print("64 bits");
            break;
    }
}    
}
